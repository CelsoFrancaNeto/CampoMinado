    public void desativarButtons() {
        for (int i = 0; i < 84; i++) {
            botoes[i].setEnabled(false);
        }
    }

    public void ativarButtons() {
        for (int i = 0; i < 84; i++) {
            botoes[i].setEnabled(true);
            botoes[i].setDisabledIcon(jButton10.getDisabledIcon());
        }
    }

    public void jogar(JButton button, int posicao) {
        if (campo.confereCasa(posicao)) {
            button.setDisabledIcon(new javax.swing.ImageIcon("bomba.png"));
            button.setEnabled(false);
            desativarButtons();
            jLabel1.setText("Boom! Voc perdeu!");
        } else {
            button.setEnabled(false);
            if (campo.addAcertos()) {
                desativarButtons();
                jLabel1.setText("Parabns, voc ganhou!");
                jLabel1.setForeground(Color.GREEN);
            }
        }
    }

